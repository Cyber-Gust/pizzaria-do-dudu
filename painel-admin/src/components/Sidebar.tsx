export default function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const supabase = createClient();

  const handleSignOut = async () => {
    await supabase.auth.signOut();
    router.push('/login');
  };

  const navItems = [
    { href: '/dashboard', icon: <HomeIcon />, label: 'Dashboard' },
    { href: '/dashboard/pedidos', icon: <OrdersIcon />, label: 'Pedidos' },
    { href: '/dashboard/cardapio', icon: <MenuIcon />, label: 'Cardápio' },
    { href: '/dashboard/estoque', icon: <StockIcon />, label: 'Estoque' },
    { href: '/dashboard/financeiro', icon: <CashFlowIcon />, label: 'Financeiro' },
    { href: '/dashboard/relatorios', icon: <ReportsIcon />, label: 'Relatórios' },
    { href: '/dashboard/configuracoes', icon: <SettingsIcon />, label: 'Configurações' },
  ];

  return (
    <aside className="w-64 bg-gray-800 text-white flex flex-col">
      <div className="p-4 border-b border-gray-700">
        <h1 className="text-2xl font-bold text-center">Forneria 360</h1>
      </div>
      <nav className="flex-1 p-2 space-y-2">
        {navItems.map((item) => (
          <Link
            key={item.label}
            href={item.href}
            className={`flex items-center space-x-3 p-3 rounded-lg transition-colors ${
              pathname === item.href
                ? 'bg-red-600 text-white'
                : 'text-gray-300 hover:bg-gray-700 hover:text-white'
            }`}
          >
            {item.icon}
            <span>{item.label}</span>
          </Link>
        ))}
      </nav>
      <div className="p-2 border-t border-gray-700">
        <button
          onClick={handleSignOut}
          className="w-full flex items-center space-x-3 p-3 rounded-lg text-gray-300 hover:bg-gray-700 hover:text-white transition-colors"
        >
          <LogoutIcon />
          <span>Sair</span>
        </button>
      </div>
    </aside>
  );
}
